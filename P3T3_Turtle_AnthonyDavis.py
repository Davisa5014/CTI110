# We use import to activate turtle
import turtle

# Draw a square
turtle.pencolor ("blue")
turtle.forward (100)
turtle.right (90)
turtle.forward (100)
turtle.right (90)
turtle.forward (100)
turtle.right (90)
turtle.forward (100)
# Draw a triangle
turtle.pencolor ("red")
turtle.right (30)
turtle.forward (100)
turtle.right (120)
turtle.forward (100)
# Write Hello World
turtle.right (180)
turtle.hideturtle ()
turtle.penup ()
turtle.forward (100)
turtle.pencolor ("green")
turtle.write ('Hello World')


